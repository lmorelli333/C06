#include<unistd.h>

int	main(int ac, char **argv)
{
	int	i;

	i = 0;
	while (--ac > 0)
	{
		while (argv[ac][i] != '\0')
		{
			write(1, &argv[ac][i], 1);
			i++;
		}
		write(1, "\n", 1);
		i = 0;
	}
}
